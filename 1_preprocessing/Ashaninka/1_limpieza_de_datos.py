corpus_original = corpus.copy()

import re

# ----------------------------------------
# 1. NORMALIZACIÓN
# ----------------------------------------
def limpiar_texto(texto):
    texto = texto.lower()
    texto = texto.strip()
    texto = re.sub(r"\s+", " ", texto)
    return texto

corpus["source"] = corpus["source"].apply(limpiar_texto)
corpus["target"] = corpus["target"].apply(limpiar_texto)


# ----------------------------------------
# 2. ELIMINAR RUIDO (palabras sueltas)
# ----------------------------------------
def es_ruido(texto):
    return len(texto.split()) <= 1

corpus = corpus[
    ~corpus["source"].apply(es_ruido) &
    ~corpus["target"].apply(es_ruido)
]


# ----------------------------------------
# 3. FILTRADO POR LONGITUD (flexible)
# ----------------------------------------
corpus = corpus[
    (corpus["source"].str.len() > 2) &
    (corpus["target"].str.len() > 2)
]


# ----------------------------------------
# 4. FILTRADO POR DESBALANCE
# ----------------------------------------
corpus = corpus[
    abs(corpus["source"].str.len() - corpus["target"].str.len()) < 150
]


# ----------------------------------------
# 5. ELIMINAR PARES IDÉNTICOS
# ----------------------------------------
corpus = corpus[
    corpus["source"] != corpus["target"]
]


# ----------------------------------------
# 6. ELIMINAR DUPLICADOS
# ----------------------------------------
corpus = corpus.drop_duplicates()


# ----------------------------------------
# 7. RESET INDEX
# ----------------------------------------
corpus = corpus.reset_index(drop=True)


