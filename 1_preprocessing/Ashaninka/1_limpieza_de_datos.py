#Método para leer archivos
def leer_txt(path):
    with open(path, "r", encoding="utf-8") as f:
        return [l.strip() for l in f.readlines() if l.strip()]

data = []

def cargar_cuentos_ashaninka(ruta, nombre_dataset):

    archivos = os.listdir(ruta)

    # Español
    files_es = sorted([f for f in archivos if f.endswith(".es")])

    # Asháninka / Ashéninka
    files_amazonico = sorted([
        f for f in archivos if f.endswith(".cni") or f.endswith(".cpu")
    ])

    print(f"\n--- {nombre_dataset.upper()} ---")
    print("ES:", files_es)
    print("ASHANINKA/ASHENINKA:", files_amazonico)

    # ----------------------------------------
    # EMPAREJAR ARCHIVOS POR NOMBRE EXACTO
    # ----------------------------------------
    pares = []

    for f_es in files_es:
        base_es = f_es.replace(".es", "")  # ej: es-cni1

        for f_am in files_amazonico:
            base_am = f_am.replace(".cni", "").replace(".cpu", "")

            # MATCH EXACTO
            if base_es == base_am:
                pares.append((f_es, f_am))

    print("Total pares encontrados:", len(pares))

    # Verificación extra (opcional pero recomendable)
    if len(pares) != len(files_es):
        print(" Advertencia: algunos archivos ES no tienen pareja")

    # ----------------------------------------
    # PROCESAR LOS PARES
    # ----------------------------------------
    for f_es, f_am in pares:

        path_es = os.path.join(ruta, f_es)
        path_am = os.path.join(ruta, f_am)

        es_lines = leer_txt(path_es)
        am_lines = leer_txt(path_am)

        # Validación de alineación
        if len(es_lines) != len(am_lines):
            print(f"Desalineado: {f_es} ↔ {f_am}")
            print("ES:", len(es_lines), "| AM:", len(am_lines))
            continue

        for es, am in zip(es_lines, am_lines):
            data.append({
                "source": am,
                "target": es,
                "dataset": nombre_dataset
            })

    print("Total ejemplos acumulados:", len(data))

cargar_cuentos_ashaninka(ruta_cuentos, "cuentos")

