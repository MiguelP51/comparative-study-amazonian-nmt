import pandas as pd

def cargar_flashcards(ruta, nombre_dataset):

    archivos = os.listdir(ruta)

    files_xlsx = [f for f in archivos if f.endswith(".xlsx")]

    print(f"\n--- {nombre_dataset.upper()} ---")
    print("Total archivos:", len(files_xlsx))

    for file in files_xlsx:

        path = os.path.join(ruta, file)

        try:
            # Leer sin header (porque no hay encabezados)
            df = pd.read_excel(path, header=None)

            print(f"\nProcesando: {file}")

            # Tomar solo columnas A y B (0 y 1)
            df = df[[0, 1]]

            # Eliminar filas vacías
            df = df.dropna()

            for _, row in df.iterrows():

                es = str(row[0]).strip()
                am = str(row[1]).strip()

                # Evitar filas vacías o raras
                if es == "" or am == "":
                    continue

                data.append({
                    "source": am,
                    "target": es,
                    "dataset": nombre_dataset
                })

        except Exception as e:
            print(f"Error en {file}: {e}")

    print("Total ejemplos acumulados:", len(data))

cargar_flashcards(ruta_flashcards, "flashcards")

