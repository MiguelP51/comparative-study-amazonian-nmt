#Método para leer archivos
def leer_txt(path):
    with open(path, "r", encoding="utf-8") as f:
        return [l.strip() for l in f.readlines() if l.strip()]

data = []

def cargar_dataset(ruta, nombre_dataset):

    archivos = os.listdir(ruta)

    # Español
    files_es = sorted([f for f in archivos if f.endswith(".es")])

    # Shipibo (puede ser .shi o .shp)
    files_shipibo = sorted([f for f in archivos if f.endswith(".shi") or f.endswith(".shp")])

    print(f"\n--- {nombre_dataset.upper()} ---")
    print("ES:", files_es)
    print("SHIPIBO:", files_shipibo)

    for f_es, f_shi in zip(files_es, files_shipibo):

        path_es = os.path.join(ruta, f_es)
        path_shi = os.path.join(ruta, f_shi)

        es_lines = leer_txt(path_es)
        shi_lines = leer_txt(path_shi)

        # Validación de alineación
        if len(es_lines) != len(shi_lines):
            print(f"Desalineado: {f_es}")
            print("ES:", len(es_lines), "| SHI:", len(shi_lines))
            continue

        for es, shi in zip(es_lines, shi_lines):
            data.append({
                "source": shi,
                "target": es,
                "dataset": nombre_dataset
            })

cargar_dataset(ruta_religioso, "religioso")
cargar_dataset(ruta_cuentos, "cuentos")
cargar_dataset(ruta_educativo, "educativo")

