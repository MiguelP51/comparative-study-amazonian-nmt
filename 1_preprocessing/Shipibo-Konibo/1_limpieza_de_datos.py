import re

# ----------------------------------------
# 1. Normalización básica
# ----------------------------------------
def limpiar_texto(texto):
    texto = texto.lower()                      # convertir a minúsculas
    texto = texto.strip()                      # eliminar espacios extremos
    texto = re.sub(r"\s+", " ", texto)         # normalizar espacios
    return texto

corpus["source"] = corpus["source"].apply(limpiar_texto)
corpus["target"] = corpus["target"].apply(limpiar_texto)


# ----------------------------------------
# 2. Eliminación de ruido (palabras sueltas)
# ----------------------------------------
def es_ruido(texto):
    return len(texto.split()) <= 1             # elimina palabras aisladas

corpus = corpus[
    ~corpus["source"].apply(es_ruido) &
    ~corpus["target"].apply(es_ruido)
]


# ----------------------------------------
# 3. Filtrado por longitud mínima (AJUSTADO)
# ----------------------------------------
corpus = corpus[
    (corpus["source"].str.len() > 2) &
    (corpus["target"].str.len() > 2)
]


# ----------------------------------------
# 4. Filtrado por desbalance (AJUSTADO)
# ----------------------------------------
corpus = corpus[
    abs(corpus["source"].str.len() - corpus["target"].str.len()) < 150
    #En Yine era 100 → más flexible para Shipibo
]


# ----------------------------------------
# 5. Eliminación de pares idénticos
# ----------------------------------------
corpus = corpus[
    corpus["source"] != corpus["target"]
]


# ----------------------------------------
# 6. Eliminación de duplicados
# ----------------------------------------
corpus = corpus.drop_duplicates()


# ----------------------------------------
# 7. Resetear índices
